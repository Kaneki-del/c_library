#include "libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t dstsize)
{
	size_t	i;

	i = ft_strlen(src);
	while (src)
		*(dst++) = *(src++);
	*dst = '\0';
	return (i);
}
